                        <h2>
                            API's en el intercambio de información entre aplicaciones de redes sociales.
                        </h2>
                        <p>
                            Programación de API’s sociales.
                            <img src="img/redes.png" align="right" alt="">
                        <p>
                            • Las redes sociales poseen información personal y de opinión de los usuarios
                        </p>

                        <p>
                            • Para conocer esa información con fines específicos y promocionar marcas y servicios, se diseñan aplicaciones que se conecten mediante el API (Application Programming Interface) de dichas redes
                        </p>
                        • Para conectarse se sigue un proceso de autenticación y autorización de permisos mediante el protocolo oAuth (Open Authentication)
                        <p>• Este protocolo permite al usuario conceder acceso a un tercero (proveedor de servicio o aplicación) a sus datos sin proporcionar su usuario y contraseña </p>
                        </p>

                        <p>

                            Las API de las redes sociales han crecido mucho desde que Google lanzó la primera versión de su API de Youtube en mayo de 2008, y desde que Facebook lanzó la primera versión de su API Graph en abril de 2010. Hoy, estas API te dan la oportunidad de buscar en las plataformas de redes sociales: posts, usuarios, canales y data demográfica. También te dejan crear tu propio servicio o conocer más sobre tu base de usuarios.
                        </p>
                        <h1>
                            Facebook
                        </h1>
                        <p>
                            El Explorador API Graph es la herramienta más usada comúnmente al trabajar con la API de Facebook. Te deja ejecutar las preguntas de la API Graph en el buscador, al igual que examinar los resultados: puedes usar uno de los bloques de acceso de tu aplicación o crear uno en el momento con scopes seleccionados.
                        </p>
                        <p>La API Graph es una API con base REST que te permite crear, actualizar y eliminar objetos de cada petición HTTP en algunos nodos.</p>
                        <h1>YouTube</h1>
                        <p>La API de YouTube nos permite modificar el aspecto y las funciones del reproductor de videos. También es posible crear nuevas aplicaciones que permitan generar, buscar o clasificar videos en YouTube a través de una interfaz propia, en la cual podamos incluir publicidad u otro tipo de contenido. La API brinda funciones para obtener información sobre usuarios y videos.</p>
                        <h1>
                            Flickr
                        </h1>
                        <p>
                            habilita una generosa colección de métodos para el manejo de fotografías, galerías, favoritos, grupos, foros de consulta e información de usuario.
                        </p>
                        <p>
                            Cuando el usuario autoriza a la app, la red social le proporciona un “token” para realizar peticiones (leer información, intereses, contactos, publicar). La interacción entre las redes sociales y la App se realiza mediante peticiones HTTPS, Dependiendo de la acción a realizar se usan peticiones de GET, POST, PUT o DELETE. Todas las peticiones incluyen el “token” para validar la petición y aceptar o rechazar según los permisos otorgados. El “token” se envía en el encabezado de las peticiones (puede ser enviado como parámetro en la URL protegido por el HTTPS
                        </p>
                        <h2>
                            Elementos del lenguaje de programación necesarios para el uso de API's de redes sociales
                        </h2>
