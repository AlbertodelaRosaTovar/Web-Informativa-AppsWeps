                        <h1>
                            API's en el intercambio de información entre aplicaciones de plataformas online.
                        </h1>
                        <p>Las API permiten que sus productos y servicios se comuniquen con otros, sin necesidad de saber cómo están implementados. Esto simplifica el desarrollo de las aplicaciones y permite ahorrar tiempo y dinero. Las API le otorgan flexibilidad; simplifican el diseño, la administración y el uso de las aplicaciones; y ofrecen oportunidades de innovación, lo cual es ideal al momento de diseñar herramientas y productos nuevos (o de gestionar los actuales).</p>
                        <p>
                            Los desarrolladores son muy conscientes de que hay cientos de APIs para hacer casi cualquier cosa en línea que te puedas imaginar. Algunas son mejores que Otras, y algunas son definitivamente más usables que otras.
                        </p>
                        <p>
                            Por ejemplo, imagínese una empresa distribuidora de libros que podría ofrecer a los clientes una aplicación de nube que les permitiera a los empleados de la librería verificar la disponibilidad de los libros con el distribuidor. El desarrollo de la aplicación podría ser costoso, verse limitado por la plataforma, llevar mucho tiempo y requerir mantenimiento permanente.
                        </p>
                        <p>
                            Otra opción es que la distribuidora de libros proporcionara una API para verificar la disponibilidad en inventario. Existen varios beneficios de este enfoque:
                        <p> • Permite que los clientes accedan a los datos con una API que les ayude a añadir información sobre su inventario en un solo lugar.</p>
                        <p> • La distribuidora de libros podría realizar cambios en sus sistemas internos sin afectar a los clientes, siempre y cuando el comportamiento de la API fuera el mismo.</p>
                        <p> • Con una API disponible de forma pública, los desarrolladores que trabajan para la distribuidora de libros, los vendedores o los terceros podrían desarrollar una aplicación para ayudar a los clientes a encontrar los libros que necesiten. Esto podría dar como resultado mayores ventas u otras oportunidades comerciales.</p>
                        </p>
                        <p>En resumen, las API le permiten ofrecer acceso a los recursos y, al mismo tiempo, mantener la seguridad y el control. Usted decide cómo habilita el acceso y a quiénes se lo otorga. La seguridad de las API depende de su buena gestión, lo cual incluye el uso de una puerta de enlace de API. Para conectarse a las API y crear aplicaciones que utilicen los datos o las funciones que ofrecen, se puede utilizar una plataforma de integración distribuida que conecte todos los elementos, como los sistemas heredados y el Internet de las cosas (IoT).</p>
                        <h2> Elementos del lenguaje de programación necesarios para el uso de API's de plataformas online.</h2>
                        <p>Echemos un vistazo rápido al índice de referencia de API de Twitter para ver un ejemplo:</p>
                        <img src="img/tw.png">
                        <p>Si observas la captura de pantalla anterior, notarás que hay varias categorías de información disponibles para los desarrolladores externos. Una vez que seleccionas una API que te interesa, puedes hacer clic en ella para ver qué información hay disponible a través de esta API. Observa la API de línea de tiempo de tuits de Twitter a continuación.</p>
                        <img src="img/gt.png">
                        <p>En la documentación anterior, la API de línea de tiempo de Twitter explica cómo puedes tomar una breve recopilación de tuits recientes de un usuario específico y mostrarlos, con la opción de hacer clic en ellos, en tu propio sitio web. </p>
                        <h1>Cómo aplicar la API a tu sitio web</h1>
                        <p>Si agregas la URL de recursos de la API al backend de tu sitio web, este devolverá la información que solicitaste al frontend del sitio web. Puedes ver reflejado todo esto en el sitio web de New York Road Runners, donde lo usan como ayuda para promocionar la Maratón de la Ciudad de Nueva York (con algunas modificaciones de diseño personalizadas):</p>
                        <img src="img/tw2.png">
                        <p>Una cosa a tener en cuenta: para poder usar oficialmente una API de desarrollador, es probable que también le asignen una clave.</p>

