                        <h1>
                            ¿Qué es una API de Geolocalización?
                        </h1>
                        <img src="img/geo.png" align="left" alt="">
                        <p>
                            API es el acrónimo de Application Programming Interface (Interfaz de Programación de Aplicaciones). Se trata de una interfaz que permite la comunicación entre dos sistemas o plataformas diferentes, posibilitando agregar diversas funciones a sitios web y aplicaciones.
                        </p>
                        <p>
                            Las APIs de Geolocalización son herramientas que permiten ubicar dispositivos con gran precisión en cualquier lugar de la superficie del planeta. Para hacerlo, se basan en señales de teléfonos móviles, GPS y WiFi que son detectadas por el dispositivo. Además, realizan cálculos para definir la mejor ruta entre distintos puntos teniendo en cuenta el tiempo y la distancia, entre otras funcionalidades.
                        </p>
                        <br>
                        <br>
                        <br>
                        <h1>
                            ¿Para qué sirve una API de Geolocalización?
                        </h1>
                        <p>
                            La API de Geolocalización es capaz de integrar el sistema de tu empresa con plataformas de mapas como Google Maps.
                            Estas APIs tienen varias funciones, como detallamos anteriormente. Además, pueden localizar dispositivos con sus usuarios, permitiendo una serie de servicios y ventajas que veremos a continuación.
                        </p>
                        <h1>
                            Ventajas de las API's en el intercambio de información entre aplicaciones de geolocalización
                        </h1>
                        <ol>
                            <li>Facilita el acceso a tu empresa.Las APIs de geolocalización ayudan a los compradores a encontrar la mejor tienda para visitar, teniendo en cuenta parámetros como mayor cercanía, menor tiempo de espera, productos disponibles, facilidad de llegada y horarios de atención.
                            </li>
                            <br>
                            <li>Te ayuda a conocer a tu audiencia, haciendo tu marketing más eficiente.
                                La API de Geolocalización es la base del Geomarketing, una estrategia de marketing que se desarrolla a partir de datos de geolocalización, implementando acciones en función de los lugares donde las personas viven, circulan o frecuentan.Este tipo de estrategia permite a una empresa conocer mejor a su público objetivo, entendiendo mejor sus necesidades, preferencias y patrones de consumo.
                            </li>
                            <br>
                            <li> Incrementa la productividad.Un ejemplo claro de uso de la API de geolocalización es la actividad de enrutamiento que se vuelve más eficiente al agrupar paradas por proximidad estableciendo las mejores rutas. Esto permite realizar un mayor número de entregas en un menor tiempo. Una de las grandes ventajas para este proceso es el cálculo en tiempo real y poder aplicar reglas comerciales.
                            </li>
                        </ol>

                        <h1>Geolocalización con HTML5</h1>
                        <p>Html5 nace con una serie de interesantes librerías que facilitan al programador la interacción del usuario con sus aplicaciones web, para el caso actual usamos la API de geolocalización. </p>
                        <p>Está permite localizar al usuario de una manera sencilla con unas simples instrucciones que nos devolverán la latitud y longitud del usuario entre otra información. Su uso es muy sencillo, con un pequeño ejemplo lo veremos mejor:</p>
                        <img src="img/codigo.png" alt="">

                        <p>El objeto posicion se pasa por referencia y si la función tiene exito viene cargado con todos los siguientes datos:</p>
                        <ul>
                            <li>
                                Latitude => Variable latitud en grados
                            </li>
                            <li>
                                Longitude => Variable longitud en grados
                            </li>
                            <li>
                                Acurracy => Precisión aproximada en metros
                            </li>
                            <li>
                                Altitude => Altura en metros
                            </li>
                            <li>
                                AltitudeAcurracy => Precisión aproximada de la altura en metros
                            </li>
                            <li>
                                Heading => Orientación en grados
                            </li>

                        </ul>
                        <p>Recibimos como parámetros ambas coordenadas, el unico "inconveniente" es que el usuario tiene que aceptar el permiso explícitamente para que funcione, por lo que se necesita una interacción previa con el usuario para poder usarlo.</p>
