                        <h1>
                            Plataformas SOA
                        </h1>
                        <h1>Plataforma MICROSOFT</h1>
                        <p>La Arquitectura Orientada a Servicios de Microsoft está basada en todos y cada uno de los elementos de la pila de tecnologías de Microsoft, desde las herramientas de desarrollo para crear servicios Web como .NET a productos de servidor, como BizTalk Server y Microsoft Office SharePoint Server, donde se produce la ejecución posterior de los servicios Web al conectar y orquestar servicios, y finalmente en las aplicaciones compuestas que consumen servicios Web (como pueden ser aplicaciones basadas en Web disponibles en entornos de intranet, extranet o Internet, así como las aplicaciones de cliente avanzadas desarrolladas con tecnologías de Microsoft Office o de cliente avanzado).</p>
                        <h1>Creación de servicios Web</h1>
                        <p>Microsoft mantiene su compromiso de fomentar el desarrollo para la creación y gestión de sistemas interconectados, basando por completo su plataforma de desarrollo de última generación en los servicios Web con Microsoft .NET.</p>

                        <h1>.NET Framework 3.0</h1>
                        <p>Para los desarrolladores de software, .NET Framework es el modelo de programación de código gestionado de Microsoft para la creación de aplicaciones sobre plataforma Windows. proporcionando el soporte necesario para los servicios Web, de manera que los desarrolladores puedan codificar, descubrir, depurar, instalar y consumir servicios Web utilizando cualquiera de los más de 20 lenguajes de programación soportados por este entorno.</p>
                        <img src="img/net.png" alt="">

                        <p>Los componentes extendidos de .NET Framework 3.0 para la creación y aprovechamiento de los servicios Web son Windows Communication Foundation (WCF), Windows Workflow Foundation (WF),Windows CardSpace, y Windows Presentation foundation.</p>


                        <h1>Visual Studio</h1>
                        <p>Visual Studio el entorno de desarrollo de aplicaciones sobre plataforma Windows, que permite el consumo de servicios Web en aplicaciones Windows, Office, aplicaciones Web, y para dispositivos móviles. Además facilita la publicación y localización de nuevos servicios Web en el seno de la empresa y permite ejecutar tests de carga sobre los servicios Web.</p>
                        <img src="img/visual.png" alt="" height="225px" width="225px">


                        <p>Microsoft dispone de tecnologías habilitadoras para el diseño, automatización, gestión y optimización de los procesos de negocio de una forma flexible. Entre ellos se encuentran:</p>
                        <h1>BizTalk Server</h1>


                        <p>Como complemento a las tecnologías de desarrollo .NET Framework 3.0, BizTalk Server es un producto de servidor orientado a los profesionales de IT y arquitectos, que permite la integración de sistemas, empleados y partners de negocio. BizTalk Server permite también la integración de aplicaciones y sistemas que no son compatibles con los servicios Web, mediante el empleo de una gran variedad de adaptadores.</p>
                        <h1>Microsoft Office SharePoint Server</h1>
                        <img src="img/sh.png" alt="">
                        <p>Está diseñado para optimizar la forma en que las personas interactúan con los contenidos y los procesos dentro de las organizaciones y a través de ellas. Proporciona una experiencia de usuario sencilla y consistente, gracias a aplicaciones de cliente muy conocidas y con ello hace que las tareas de iniciación de procesos de negocio de tipo manual, la participación en estos procesos, su seguimiento y la elaboración de informes sea mucho más sencilla y flexible.</p>



