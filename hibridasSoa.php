                        <h1>
                            ¿Que son las Aplicaciones Web hibridas orientadas a servicios.?
                        </h1>
                        <img src="img/aso.jpg" align="left" alt="" height="225px" width="225px">
                        <p>Las aplicaciones híbridas SOA son esencialmente aplicaciones web que se han colocado en un shell de aplicación nativo. Una vez que se descargan de una tienda de aplicaciones y se instalan localmente, el shell puede conectarse a cualquier capacidad que brinde la plataforma móvil a través de un navegador integrado en la aplicación. El navegador y sus complementos se ejecutan en el back-end y son invisibles para el usuario final.</p>
                        <p>Las aplicaciones híbridas SOA son populares porque permiten a los desarrolladores escribir código para una aplicación móvil una vez y aún se adaptan a múltiples plataformas. Debido a que las aplicaciones híbridas agregan una capa adicional entre el código fuente y la plataforma de destino, pueden funcionar un poco más lento que las versiones nativas o web de la misma aplicación.</p>

                        <br>
                        <br>
                        <br>
                        <br>

                        <h1>Características de las aplicaciones híbridas SOA</h1>

                        <ul>
                            <li>La capacidad de funcionar esté o no conectado el dispositivo.</li>
                            <li>Integración con el sistema de archivos del dispositivo móvil.</li>
                            <li>Integración con servicios basados en web.</li>
                            <li>Un navegador integrado para mejorar el acceso a contenido dinámico en línea.</li>
                        </ul>

                        <h1>¿Cómo funcionan?</h1>

                        <p>Las aplicaciones híbridas orientadas a servicios funcionan de manera similar a las aplicaciones web pero, al igual que las aplicaciones nativas, se descargan en el dispositivo. Al igual que las aplicaciones web, las aplicaciones híbridas suelen estar escritas en HTML5, CSS y JavaScript. Las aplicaciones híbridas ejecutan código dentro de un contenedor. El motor del navegador del dispositivo se utiliza para representar HTML y JavaScript y API nativas para acceder al hardware específico del dispositivo.</p>
                        <p>Aunque una aplicación híbrida normalmente compartirá elementos de navegación similares a los de una aplicación web, el hecho de que una aplicación funcione sin conexión depende de sus funcionalidades. Si una aplicación no necesita soporte de una base de datos, entonces se puede hacer que funcione sin conexión.</p>

                        <h1>Pros y contras</h1>
                        <h1>Las ventajas de las aplicaciones híbridas SOA incluyen:</h1>
                        <ul>
                            <li>Operar en diferentes plataformas.</li>
                            <li>Tiempos de compilación más rápidos en comparación con las aplicaciones nativas.</li>
                            <li>Más barato de desarrollar en comparación con la creación de dos versiones de una aplicación nativa para dos plataformas diferentes.</li>
                            <li>Es más fácil lanzar parches y actualizaciones.</li>
                            <li>Puede trabajar en línea y sin conexión.</li>
                        </ul>
                        <h1>Sin embargo, algunas desventajas incluyen:</h1>
                        <ul>
                            <li>Variaciones debidas al desarrollo inclinado en una plataforma; por ejemplo, si un equipo de desarrollo inclina su trabajo hacia una plataforma, otra plataforma admitida puede carecer de calidad o sufrir errores.</li>
                            <li>La apariencia de una aplicación puede variar de una plataforma a otra.</li>
                            <li>La necesidad de probar la aplicación en una variedad de dispositivos para garantizar un funcionamiento adecuado.</li>
                            <li>La experiencia del usuario (UX) puede caer si la interfaz de usuario (UI) no es similar a los navegadores a los que el usuario está acostumbrado y está lo suficientemente bien diseñada.</li>
                        </ul>

