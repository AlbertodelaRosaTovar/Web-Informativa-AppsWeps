                        <img src="img/api.png" alt="">
                        <p>Hay pocas sorpresas en esta información. Google Maps es, con mucho, la API más popular, con un 50% de los mashups que la utilizan. Flickr y Amazon le siguen con un 11% y un 8% respectivamente. Además, es notable que el 5% de los mashups utilizan YouTube, lo que probablemente corresponde al reciente aumento de agregadores de videos como Vidmeter. Finalmente, el 4% y un total de 74 mashups utilizan la API del.icio.us.</p>
