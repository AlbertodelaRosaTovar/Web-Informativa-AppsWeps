                        <h1>
                            ¿Que es Mashup?
                        </h1>
                        <img src="img/mashup.png" align="left" alt="" height="225px" width="225px">
                        <p>Una Mashup o aplicación web híbrida es una aplicación que combina servicios de varias páginas web en una sola para ofrecer un nuevo servicio. Es habitual que la integración de datos se obtenga desde APIs abiertas. La libertad para integrar todas estas fuentes supone una oportunidad para desarrollar nuevos servicios en muy poco tiempo, al basarse en funcionalidades ya existentes.</p>
                        <p>Típicamente los mashups se basan en utilizar contenido de terceros a través de fuentes o interfaces públicas. Entre las fuentes más utilizadas para crear mashups destacan Google Maps, Amazon, Flickr, Youtube, Ebay y Yahoo. La facilidad con la que estas plataformas permiten integrar su contenido ha propiciado un gran éxito para las aplicaciones web híbridas.</p>

                        <p>El término proviene de una expresión anglosajona para designar la creación de una canción a partir de la mezcla de otras composiciones, normalmente de estilos muy diferentes. Este origen ilustra muy bien en que consiste un mash up y cómo se forma. El término musical ha dado lugar al nombre de la aplicación web híbrida a partir del desarrollo de la web 2.0.</p>

                        <h1>Tipos de mashups</h1>
                        <p>No existe unanimidad entre las distintas clasificaciones de mashups que pueden hacerse. La Wikipedia, por ejemplo, distingue entre mashups de consumidores (el más conocido), mashups de datos y mashups empresariales. Otras fuentes señalan que una web híbrida puede ser de datos, fotos (Flickr) o vídeos (Youtube); de compras (Amazon) o noticias; o clasificados en función de su construcción (primer tipo de aplicación híbrida y segundo tipo de aplicación híbrida).</p>
                        <p>En lo que sí coinciden los expertos en tecnología de la información es en que son aplicaciones centradas en el usuario, realizadas por y para él, ya que su interés último suele ser compartir la información con otras personas. Si se trata de mashups empresariales, el usuario también es el objetivo sobre el cual se construyen.</p>
